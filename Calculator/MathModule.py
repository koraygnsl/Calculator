def add(x,y):
    result = x + y
    print(float(result))
def subtract(x,y):
    result = x - y
    print(float(result))
def divide(x,y):
    result = x*y
    print(float(result))
def multiply(x,y):
    result = x/y
    print(float(result))
def exponentiation(x,y):
    result = x**y
    print(float(result))       

